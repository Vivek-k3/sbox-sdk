interface PageActionsProps {
  /** Pre-rendered on the server and passed down, so copy works with no fetch. */
  markdown: string;
  /** Relative URL for the in-app "view raw" link, e.g. `/overview.md`. */
  markdownUrl: string;
  /** Absolute URL so the chat targets can actually fetch the page. */
  markdownAbsoluteUrl: string;
  /** Source file on GitHub. */
  githubUrl: string;
}

export const PageActions = ({
  markdown,
  markdownUrl,
  markdownAbsoluteUrl,
  githubUrl,
}: PageActionsProps) => {
  const [copied, setCopied] = useState(false);

  const handleCopy = async () => {
    try {
      await navigator.clipboard.writeText(markdown);
      setCopied(true);
      setTimeout(() => setCopied(false), 1800);
      posthog.capture("docs_markdown_copied", {
        page_url: markdownAbsoluteUrl,
      });
    } catch {
      // clipboard unavailable — ignore
    }
  };

  const prompt = `Read ${markdownAbsoluteUrl}, I want to ask questions about it.`;

  return (
    <div className="not-prose mb-6 flex flex-row items-center gap-2">
      <Button onClick={handleCopy} size="sm" type="button" variant="outline">
        {copied ? (
          <Check data-icon="inline-start" />
        ) : (
          <Copy data-icon="inline-start" />
        )}
        {copied ? "Copied" : "Copy Markdown"}
      </Button>
      <DropdownMenu>
        <DropdownMenuTrigger asChild>
          <Button size="sm" variant="outline">
            Open
            <ChevronDown data-icon="inline-end" />
          </Button>
        </DropdownMenuTrigger>
        <DropdownMenuContent align="end" className="w-56">
          <DropdownMenuGroup>
            <DropdownMenuItem asChild>
              <a href={markdownUrl} rel="noopener noreferrer" target="_blank">
                <FileText />
                View as Markdown
              </a>
            </DropdownMenuItem>
            <DropdownMenuItem asChild>
              <a
                href={githubUrl}
                onClick={() =>
                  posthog.capture("docs_viewed_on_github", {
                    page_url: markdownAbsoluteUrl,
                  })
                }
                rel="noopener noreferrer"
                target="_blank"
              >
                <GitHubIcon />
                Open in GitHub
              </a>
            </DropdownMenuItem>
          </DropdownMenuGroup>
          <DropdownMenuSeparator />
          <DropdownMenuGroup>
            {chatProviders.map((provider) => (
              <DropdownMenuItem asChild key={provider.name}>
                <a
                  href={provider.createUrl(prompt)}
                  onClick={() =>
                    posthog.capture("docs_opened_in_external_chat", {
                      page_url: markdownAbsoluteUrl,
                      provider: provider.name,
                    })
                  }
                  rel="noopener noreferrer"
                  target="_blank"
                >
                  {provider.icon}
                  Open in {provider.name}
                </a>
              </DropdownMenuItem>
            ))}
          </DropdownMenuGroup>
        </DropdownMenuContent>
      </DropdownMenu>
    </div>
  );
};
